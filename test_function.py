def calculate_sum(a, b):
    """
    Calculate the sum of two numbers.
    
    Args:
        a: First number
        b: Second number
    
    Returns:
        Sum of a and b
    """
    return a + b

# Test the function
if __name__ == "__main__":
    result = calculate_sum(5, 3)
    print(f"5 + 3 = {result}")
